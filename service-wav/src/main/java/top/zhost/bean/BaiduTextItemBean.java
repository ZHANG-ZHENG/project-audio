package top.zhost.bean;

public class BaiduTextItemBean {
	
	private String formal;
	private String[] loc_details;
	private String item;
	private String pos;
	private String ne;
	private String[] basic_words;
	private Integer byte_length;
	private Integer byte_offset;
	private String uri;
	
	public String getFormal() {
		return formal;
	}
	public void setFormal(String formal) {
		this.formal = formal;
	}
	public String[] getLoc_details() {
		return loc_details;
	}
	public void setLoc_details(String[] loc_details) {
		this.loc_details = loc_details;
	}
	public String getItem() {
		return item;
	}
	public void setItem(String item) {
		this.item = item;
	}
	public String getPos() {
		return pos;
	}
	public void setPos(String pos) {
		this.pos = pos;
	}
	public String getNe() {
		return ne;
	}
	public void setNe(String ne) {
		this.ne = ne;
	}
	public String[] getBasic_words() {
		return basic_words;
	}
	public void setBasic_words(String[] basic_words) {
		this.basic_words = basic_words;
	}
	public Integer getByte_length() {
		return byte_length;
	}
	public void setByte_length(Integer byte_length) {
		this.byte_length = byte_length;
	}
	public Integer getByte_offset() {
		return byte_offset;
	}
	public void setByte_offset(Integer byte_offset) {
		this.byte_offset = byte_offset;
	}
	public String getUri() {
		return uri;
	}
	public void setUri(String uri) {
		this.uri = uri;
	}
	
	
}
